#include "mem.h"

__attribute__((hot, const, __always_inline__))
inline t_u64	__populate(t_u8 y)
{
	t_u64	x;

	x = (t_u64)y;
	x |= x << 8;
	x |= x << 16;
	x |= x << 24;
	x |= x << 32;
	x |= x << 40;
	x |= x << 48;
	x |= x << 56;
	return (x);
}
